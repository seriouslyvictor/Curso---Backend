# Objetivo: Fazer o computador mostrar mensagens na tela
# Conceito: print() é o comando que "imprime" texto na tela
# ============================================================

# -----------------------------
# PARTE 1: Olá, Mundo!
# -----------------------------
# Este é o programa mais famoso do mundo!
# Todo programador começa assim.

print("Olá, mundo!")

# O que aconteceu?
# - print() é uma FUNÇÃO (um comando que faz algo)
# - As aspas "" indicam que é TEXTO
# - O computador mostrou exatamente o que está entre aspas


# -----------------------------
# PARTE 2: Várias mensagens
# -----------------------------
# Você pode usar print() quantas vezes quiser

